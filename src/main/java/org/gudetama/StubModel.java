package org.gudetama;

public class StubModel {
	private String stubId;
	private String stubName;
	private String stubDescription;
	public String getStubId() {
		return stubId;
	}
	public void setStubId(String stubId) {
		this.stubId = stubId;
	}
	public String getStubName() {
		return stubName;
	}
	public void setStubName(String stubName) {
		this.stubName = stubName;
	}
	public String getStubDescription() {
		return stubDescription;
	}
	public void setStubDescription(String stubDescription) {
		this.stubDescription = stubDescription;
	}
	
	
}
